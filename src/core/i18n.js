/**
 * Get translation for a key
 * @param {string} langCode - Language code
 * @param {string} key - Translation key (e.g., 'ui.analyzing' or 'content.defaultTitle')
 * @param {string} fallback - Fallback text if translation not found
 * @returns {string}
 */
export function t(langCode, key, fallback = '') {
    const lang = langCode || 'en';
    const keys = key.split('.');
    let value = translations[lang];

    // Fallback to English if language not found
    if (!value) {
        value = translations.en;
    }

    for (const k of keys) {
        if (value && typeof value === 'object' && k in value) {
            value = value[k];
        } else {
            // Try English fallback
            let enValue = translations.en;
            for (const enK of keys) {
                if (enValue && typeof enValue === 'object' && enK in enValue) {
                    enValue = enValue[enK];
                } else {
                    return fallback || key;
                }
            }
            return enValue || fallback || key;
        }
    }

    return value || fallback || key;
}

/**
 * Get all UI translations for a language
 * @param {string} langCode - Language code
 * @returns {Object}
 */
export function getUITranslations(langCode) {
    const lang = langCode || 'en';
    return translations[lang]?.ui || translations.en.ui;
}

/**
 * Get all content translations for a language
 * @param {string} langCode - Language code
 * @returns {Object}
 */
export function getContentTranslations(langCode) {
    const lang = langCode || 'en';
    return translations[lang]?.content || translations.en.content;
}

/**
 * Check if a language is supported
 * @param {string} langCode - Language code
 * @returns {boolean}
 */
export function isLanguageSupported(langCode) {
    return langCode in translations;
}
